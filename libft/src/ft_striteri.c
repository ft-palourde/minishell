#include "libft.h"

void	ft_striteri(char const *s, void (*f)(unsigned int, char*));

void	ft_striteri(char const *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;

	i = 0;
	if (s == NULL || f == NULL)
		return ;
	while (*s != '\0')
	{
		(*f)(i, (char *)s);
		s++;
		i++;
	}
}
